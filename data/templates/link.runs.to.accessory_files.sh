getIdCols() {
  mip_id=$(getHeader $1|grep mip_id|cut -f1);
  if [[ -n $mip_id ]];
    then cols=1;
  else \
    cols=$(getHeader $1|grep -P "(mip_pick_count|rank_score)"|cut -f1|tr '\n' ','|sed 's/,$//g');
  fi;
  echo $cols
}

getExtLigSeqCols() {
  cols=$(getHeader $1|grep -P "(ext|lig).*sequence"|cut -f1|tr '\n' ','|sed 's/,$//g');
  echo $cols
}
